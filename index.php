                    <h5>So what’s a “Bonovum”?</h5>
                    <p>Bonovum, a portmanteau of the Latin <em>bonum</em> for &ldquo;good&rdquo; and <em>ovum</em> for &ldquo;egg,&rdquo; literally means &ldquo;good egg.&rdquo; To quote Ma &amp; Pa Humble: &ldquo;
                    You’re a pretty good egg, aren’t ya?&rdquo; I could think of no better name for my enterprise, the idea for which has slowly incubated for several years. Both a personal moniker and a tribute to the unwavering support of my parents, Bonovum Creative seeks to hatch only the best ideas. Besides, anyone can slap the word &ldquo;design&rdquo; after their name, but where’s the creativity in that?</p>
